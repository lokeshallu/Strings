package com.kn.strings;

public class StringMethodConcat {

	public static void main(String[] args) {
	 String s1= "Ram";
	 String s2 = " sita";
	 
	 System.out.println(s1);
	 System.out.println(s2);
	 
	String s3=s1.concat(s2);
	 System.out.println(s1);
	 System.out.println(s2);
	 System.out.println(s3);

	}

}
